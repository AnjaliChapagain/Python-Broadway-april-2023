# Python numbers are of three types

n1 = 12 # Integer Type
print(type(n1))

n2 = 13.5 #Floast Type
print(type(n2))

n3 = 3 + 5j # complex type
print(type(n3))

n1 = 3e2 # This is 3 * 10 **2 . It gives 300.0
print(n1)
n2 = 3e-2 # This is 3 * 10 ** -2. It gives 0.003
print(n2)

bin = 0b101010 # This is binary number
oct = 0o75 # This is an octal number
hex = 0xA2F # This is hexadecimal number

